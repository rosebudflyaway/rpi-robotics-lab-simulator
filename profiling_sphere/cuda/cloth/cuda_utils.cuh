

// float version of atomic add
__device__  float atomicAddf(float* address, float val) {
   float old = *address, assumed;
   do { assumed = old;
        old = __int_as_float( 
            atomicCAS((unsigned int*)address,
            __float_as_int(assumed),
            __float_as_int(val + assumed)) );
   } while (assumed != old);
   return old;
} 





